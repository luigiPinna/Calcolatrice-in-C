#include "Libreria_Calcolatrice.h"
float addizione(float num1, float num2){
    return num1+num2;
}
float sottrazione(float num1, float num2){
    return num1-num2;
}
float moltiplicazione(float num1, float num2){
    return num1*num2;
}
float divisione(float num1, float num2){
    return num1/num2;
}